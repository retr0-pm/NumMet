import streamlit as st

st.title("Основные обозначения — интерполяция и сплайны")

st.markdown(r"""
| как | $\rightarrow$ | что означает |
| ----------------------: | :------: | :------------------------------ |
| $x = \{x_i\}_{i=0}^n$ | $\rightarrow$ | узлы интерполяции |
| $y_i = f(x_i)$ | $\rightarrow$ | значения функции в узлах |
| $P_n(x)$ | $\rightarrow$ | интерполяционный многочлен Ньютона степени $n$ |
| $a_k$ | $\rightarrow$ | коэффициенты многочлена Ньютона (разделённые разности) |
| $f[x_i]$ | $\rightarrow$ | разделённая разность нулевого порядка |
| $f[x_i,x_{i+1},\dots,x_{i+k}]$ | $\rightarrow$ | разделённая разность $k$-го порядка |
| $p(x)$ | $\rightarrow$ | значение интерполяционного многочлена в точке $x$ |
| $S(x)$ | $\rightarrow$ | натуральный кубический сплайн |
| $M_i = S''(x_i)$ | $\rightarrow$ | вторая производная сплайна в узле |
| $h_i = x_{i+1}-x_i$ | $\rightarrow$ | длина интервала между узлами |
| $f[x_i,x_{i+1}] = \frac{f_{i+1}-f_i}{x_{i+1}-x_i}$ | $\rightarrow$ | разделённая разность первого порядка |
| $f[x_i,x_{i+1},x_{i+2}] = \frac{f[x_{i+1},x_{i+2}] - f[x_i,x_{i+1}]}{x_{i+2}-x_i}$ | $\rightarrow$ | разделённая разность второго порядка |
| $n$ | $\rightarrow$ | число интервалов или степень многочлена |
| $f(x)$ | $\rightarrow$ | исходная функция (например, функция Рунге $f(x) = \frac{1}{1+25x^2}$) |
| $x^{(k)}$ | $\rightarrow$ | приближение решения на итерации $k$ (для численных методов) |
| $r^k = f - Ax^k$ | $\rightarrow$ | вектор невязки (если используется система) |
| $\|x\|$ | $\rightarrow$ | норма вектора $x$ |
| $S_i(x)$ | $\rightarrow$ | кубический полином сплайна на интервале $[x_i,x_{i+1}]$ |
| $S_i(x) = a_i + b_i(x-x_i) + c_i(x-x_i)^2 + d_i(x-x_i)^3$ | $\rightarrow$ | стандартная форма кубического сплайна |
| $S''(x_0) = S''(x_n) = 0$ | $\rightarrow$ | граничные условия натурального сплайна |
""")
