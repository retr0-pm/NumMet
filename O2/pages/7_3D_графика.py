    ax.plot_surface(X, Y, Z, cmap='viridis', edgecolor='none')
    ax.view_init(elev=40, azim=45)
    ax.set_xlabel(r"$x$")
    ax.set_ylabel(r"$y$")
    ax.set_zlabel(r"$z$")
    ax.set_title(r"–ù–∞—Å—Ç—Ä–æ–µ–Ω–Ω–∞—è —Å—Ü–µ–Ω–∞: $z = \sin(x)\cos(y)$", pad=25)
    st.pyplot(fig, use_container_width=False)
