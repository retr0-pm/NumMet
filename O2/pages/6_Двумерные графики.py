if menu == "–í–µ–∫—Ç–æ—Ä–Ω—ã–µ –ø–æ–ª—è":
    st.markdown("""
    #### üß≠ –í–µ–∫—Ç–æ—Ä–Ω—ã–µ –ø–æ–ª—è

    * –ò—Å–ø–æ–ª—å–∑—É—é—Ç—Å—è –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–Ω—ã—Ö –≤–µ–ª–∏—á–∏–Ω (–Ω–∞–ø—Ä–∏–º–µ—Ä, —Å–∫–æ—Ä–æ—Å—Ç–∏ –∏–ª–∏ —Å–∏–ª—ã).
    * –í –∫–∞–∂–¥–æ–π —Ç–æ—á–∫–µ (x, y) –∑–∞–¥–∞—ë—Ç—Å—è –≤–µ–∫—Ç–æ—Ä —Å –∫–æ–º–ø–æ–Ω–µ–Ω—Ç–∞–º–∏ (u, v).
    * –í `Matplotlib` —Ç–∞–∫–∏–µ –ø–æ–ª—è —Å—Ç—Ä–æ—è—Ç—Å—è —Ñ—É–Ω–∫—Ü–∏–µ–π `quiver()` –∏–ª–∏ `streamplot()`.
    """)

    Y, X = np.mgrid[-3:3:20j, -3:3:20j]
    U = -1 - X**2 + Y
    V = 1 + X - Y**2

    fig, ax = plt.subplots(figsize=(3, 2))
    ax.quiver(X, Y, U, V, color='teal')
    ax.set_title("–ü—Ä–∏–º–µ—Ä –≤–µ–∫—Ç–æ—Ä–Ω–æ–≥–æ –ø–æ–ª—è", pad=15)
    ax.set_xlabel("x")
    ax.set_ylabel("y")
    ax.set_xlim(-3, 3)
    ax.set_ylim(-3, 3)
    ax.grid(True, linestyle='--', alpha=0.6)
    st.pyplot(fig, use_container_width=False)

