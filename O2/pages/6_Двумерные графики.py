if menu == "–ü—Å–µ–≤–¥–æ—Ü–≤–µ—Ç–Ω—ã–µ –≥—Ä–∞—Ñ–∏–∫–∏":
    st.markdown("""
    #### üåà –ü—Å–µ–≤–¥–æ—Ü–≤–µ—Ç–Ω—ã–µ –≥—Ä–∞—Ñ–∏–∫–∏

    * –û—Ç–æ–±—Ä–∞–∂–∞—é—Ç –∑–Ω–∞—á–µ–Ω–∏—è —Ñ—É–Ω–∫—Ü–∏–∏ *z = f(x, y)* —á–µ—Ä–µ–∑ **—Ü–≤–µ—Ç** –≤ –∫–∞–∂–¥–æ–π —Ç–æ—á–∫–µ –ø–ª–æ—Å–∫–æ—Å—Ç–∏.
    * –ü—Ä–∏–º–µ–Ω—è—é—Ç—Å—è –¥–ª—è –ø—Ä–µ–¥—Å—Ç–∞–≤–ª–µ–Ω–∏—è —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä–Ω—ã—Ö –ø–æ–ª–µ–π, –ø–ª–æ—Ç–Ω–æ—Å—Ç–µ–π, –∏–Ω—Ç–µ–Ω—Å–∏–≤–Ω–æ—Å—Ç–µ–π —Å–∏–≥–Ω–∞–ª–æ–≤.
    * –û—Å–Ω–æ–≤–Ω—ã–µ —Ñ—É–Ω–∫—Ü–∏–∏: `imshow()`, `pcolormesh()`, `matshow()`.
    """)

    x = np.linspace(-3, 3, 200)
    y = np.linspace(-3, 3, 200)
    X, Y = np.meshgrid(x, y)
    Z = np.sin(X**2 + Y**2)

    fig, ax = plt.subplots(figsize=(3, 2))
    pcm = ax.pcolormesh(X, Y, Z, cmap='plasma', shading='auto')
    plt.colorbar(pcm, ax=ax, label="z = sin(x¬≤ + y¬≤)")
    ax.set_title("–ü—Å–µ–≤–¥–æ—Ü–≤–µ—Ç–Ω–æ–µ –ø—Ä–µ–¥—Å—Ç–∞–≤–ª–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏", pad=15)
    ax.set_xlim(-3, 3)
    ax.set_ylim(-3, 3)
    st.pyplot(fig, use_container_width=False)

