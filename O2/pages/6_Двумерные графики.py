    fig, ax = plt.subplots(figsize=(3, 2))
    cs = ax.contourf(X, Y, Z, cmap='coolwarm', levels=20)
    plt.colorbar(cs, ax=ax, label="z")
    ax.set_title("–ö–æ–Ω—Ç—É—Ä–Ω–æ–µ –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏–µ z = sin(x) * cos(y)", pad=15)
