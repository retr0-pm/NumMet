# -------------------- –û–ë–©–ï–ï –û–ü–†–ï–î–ï–õ–ï–ù–ò–ï --------------------
if menu == "–û–±—â–µ–µ –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ –ø—Ä–∏–Ω–∞–¥–ª–µ–∂–Ω–æ—Å—Ç–∏ –≥—Ä–∞—Ñ–∏–∫–∞ –∫ –¥–≤—É–º–µ—Ä–Ω—ã–º":
    st.markdown("""
    #### üìò –û–±—â–µ–µ –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ –ø—Ä–∏–Ω–∞–¥–ª–µ–∂–Ω–æ—Å—Ç–∏ –≥—Ä–∞—Ñ–∏–∫–∞ –∫ –¥–≤—É–º–µ—Ä–Ω—ã–º

    * –î–≤—É–º–µ—Ä–Ω—ã–µ –≥—Ä–∞—Ñ–∏–∫–∏ –∏—Å–ø–æ–ª—å–∑—É—é—Ç—Å—è –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è —Ñ—É–Ω–∫—Ü–∏–π **–¥–≤—É—Ö –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö**, —Ç–æ –µ—Å—Ç—å –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π –≤–∏–¥–∞:
      \[
      z = f(x, y)
      \]
    * –¢–∞–∫–∏–µ –≤–∏–∑—É–∞–ª–∏–∑–∞—Ü–∏–∏ –ø–æ–∑–≤–æ–ª—è—é—Ç –∞–Ω–∞–ª–∏–∑–∏—Ä–æ–≤–∞—Ç—å —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è, —ç–∫—Å—Ç—Ä–µ–º—É–º—ã –∏ –∏–∑–º–µ–Ω–µ–Ω–∏—è —Ñ—É–Ω–∫—Ü–∏–∏ –Ω–∞ –≤—Å–µ–π –ø–ª–æ—Å–∫–æ—Å—Ç–∏.
    * –í `Matplotlib` –¥–≤—É–º–µ—Ä–Ω—ã–µ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ –º–æ–≥—É—Ç –±—ã—Ç—å –ø–æ–∫–∞–∑–∞–Ω—ã —á–µ—Ä–µ–∑ **—Ü–≤–µ—Ç**, **–∫–æ–Ω—Ç—É—Ä—ã**, **–≤–µ–∫—Ç–æ—Ä—ã** –∏–ª–∏ **–ø–ª–æ—Ç–Ω–æ—Å—Ç—å —Ç–æ—á–µ–∫**.
    * –¢–∏–ø–æ–≤—ã–µ —Ñ—É–Ω–∫—Ü–∏–∏ –≤–∏–∑—É–∞–ª–∏–∑–∞—Ü–∏–∏: `imshow`, `pcolormesh`, `contour`, `quiver`, `hist2d`.
    """)

    st.markdown("#### –î–µ–º–æ–Ω—Å—Ç—Ä–∞—Ü–∏—è —Ñ—É–Ω–∫—Ü–∏–∏ z = sin(x) * cos(y):")

    x = np.linspace(-3, 3, 200)
    y = np.linspace(-3, 3, 200)
    X, Y = np.meshgrid(x, y)
    Z = np.sin(X) * np.cos(Y)

    fig, ax = plt.subplots(figsize=(3, 2))
    im = ax.imshow(Z, extent=[-3, 3, -3, 3], origin="lower", cmap="viridis")
    ax.set_xlabel("x")
    ax.set_ylabel("y")
    ax.set_title("–ü—Ä–∏–º–µ—Ä –¥–≤—É–º–µ—Ä–Ω–æ–π –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ z = sin(x)*cos(y)", pad=15)
    plt.colorbar(im, ax=ax, label="z")
    st.pyplot(fig, use_container_width=False)

